// background color show : card 1
document.getElementById("bgColor1").addEventListener("mouseover", function () {
  const number = Math.floor(Math.random() * 10 + 1);
  if (number === 1) {
    document.getElementById("bgColor1").style.backgroundColor = "#F16767";
  } else if (number === 2) {
    document.getElementById("bgColor1").style.backgroundColor = "#A459D1";
  } else if (number === 3) {
    document.getElementById("bgColor1").style.backgroundColor = "#3E54AC";
  } else if (number === 4) {
    document.getElementById("bgColor1").style.backgroundColor = "#443C68";
  } else if (number === 5) {
    document.getElementById("bgColor1").style.backgroundColor = "#E3DFFD";
  } else if (number === 6) {
    document.getElementById("bgColor1").style.backgroundColor = "#537FE7";
  } else if (number === 7) {
    document.getElementById("bgColor1").style.backgroundColor = "#B9F3E4";
  } else if (number === 8) {
    document.getElementById("bgColor1").style.backgroundColor = "#C92C6D";
  } else if (number === 9) {
    document.getElementById("bgColor1").style.backgroundColor = "#85CDFD";
  } else if (number === 10) {
    document.getElementById("bgColor1").style.backgroundColor = "#FFD4D4";
  }
});
